package com.javainuse.model;

public class Rule {

	private String contributionType;
	private String calculationType;
	private String calculationFrequency;
	private String contibutionRules;

	public String getContributionType() {
		return contributionType;
	}

	public void setContributionType(String contributionType) {
		this.contributionType = contributionType;
	}

	public String getCalculationType() {
		return calculationType;
	}

	public void setCalculationType(String calculationType) {
		this.calculationType = calculationType;
	}

	public String getCalculationFrequency() {
		return calculationFrequency;
	}

	public void setCalculationFrequency(String calculationFrequency) {
		this.calculationFrequency = calculationFrequency;
	}

	public String getContibutionRules() {
		return contibutionRules;
	}

	public void setContibutionRules(String contibutionRules) {
		this.contibutionRules = contibutionRules;
	}
	
	

}
